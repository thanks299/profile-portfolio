document.querySelector('.btn-hire').addEventListener('mouseenter', () => {
    document.querySelector('.btn-hire').style.backgroundColor = '#fff';
    document.querySelector('.btn-hire').style.color = '#000';
});

document.querySelector('.btn-hire').addEventListener('mouseleave', () => {
    document.querySelector('.btn-hire').style.backgroundColor = '#800080';
    document.querySelector('.btn-hire').style.color = '#fff';
});

